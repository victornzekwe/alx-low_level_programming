#include "3-calc.h"

/**
 * op_add - add function
 * @a: param
 * @b: param
 * Return: operation result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub function
 * @a: param
 * @b: param
 * Return: operation result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul function
 * @a: param
 * @b: param
 * Return: operation result
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - div function
 * @a: param
 * @b: param
 * Return: operation result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod function
 * @a: param
 * @b: param
 * Return: operation result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
